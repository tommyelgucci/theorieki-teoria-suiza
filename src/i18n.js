// Cadena de interfaz: idioma pedido → alemán → la propia clave.
export function t(key, lang, vars) {
  const entry = STRINGS[key]
  let s = (entry && (entry[lang] || entry.de)) || key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v))
  }
  return s
}

// Contenido bilingüe/multilingüe ({de, es, fr?, it?, en?, pt?}): idioma pedido → alemán.
export function tr(obj, lang) {
  if (!obj) return ''
  return obj[lang] || obj.de || ''
}

